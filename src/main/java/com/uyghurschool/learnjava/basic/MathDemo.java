package com.uyghurschool.learnjava.basic;

public class MathDemo {
    public static void main(String[] args) {
        System.out.println("10 to the power of 3 "+Math.pow(10,2));
        System.out.println("Square root of 10 "+Math.sqrt(9));
        System.out.println("Return the max: "+Math.max(10,1000));
        System.out.println("Return the min: "+Math.min(10,100));
        System.out.println("Random number: "+Math.random());
        System.out.println("Random number: "+Math.random());
        System.out.println("Return the closest integer "+Math.round(3.4));
    }
}
