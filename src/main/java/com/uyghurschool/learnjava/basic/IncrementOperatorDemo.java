package com.uyghurschool.learnjava.basic;

public class IncrementOperatorDemo {
    public static void main(String[] args) {
        int i1=1;
        int j1=1;
        i1++; //i1=i1+1;
        i1++;
        j1--;
        j1++;
        System.out.println(String.format("i1=%d",i1));
        System.out.println(String.format("j1=%d",j1));
    }
}
