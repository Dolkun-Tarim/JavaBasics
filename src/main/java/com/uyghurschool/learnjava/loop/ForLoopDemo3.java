package com.uyghurschool.learnjava.loop;

public class ForLoopDemo3 {
    public static void main(String[] args) {
        for (int i = 0; i <= 50; i = i + 2) {
            System.out.print(i + " ");
        }
        System.out.println();

        for (int j = 1; j <= 50; j = j + 2) {
            System.out.print(j + " ");
        }
        System.out.println();
        for (int i = 100; i >= 0; i--)
        {
            System.out.print(i + " ");
        }
    }
}
